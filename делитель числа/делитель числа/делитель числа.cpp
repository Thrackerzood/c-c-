#include <stdio.h>
int main()
{
	int n, i, b = 1;
	scanf_s("%d", &n);
	i = n;
	do {
		if (n % b == 0)
		{
			printf("%d", b); 

			{ 
				if (b != 1);
			} 
			{
				printf(" ");
			}
		}
		b++;
	} while (b <= n);
	{
		printf("\n"); 
	}
	do {
		if (n % i == 0 || n == 0)
		{
			printf("%d", i);
			{
				if (b != n);
			}
			{
				printf(" ");
			}
		}
		i--;
	} while (i > 0);
	{
		printf("\n");
	}
}
